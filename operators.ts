// Bitwise Operators
let A:number = 2;
let B:number = 3;

let result_1 = A & B;  // bitwise AND
console.log(result_1);

let result_2 = A | B; //bitwise OR
console.log(result_2);

let result_3 = A ^ B; //bitwise XOR
console.log(result_3);

let result_4 = (~B);  //bitwise NOT
console.log(result_4);

let result_5 = (A<<1); //left shift
console.log(result_5);

let result_6 = (A>>1);  //right shift
console.log(result_6);

let result_7 = (A>>>1); //Right shift with Zero
console.log(result_7);

